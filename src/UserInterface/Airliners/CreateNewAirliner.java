     boolean datePatternCorrect()  {
        Pattern p= Pattern.compile("^(0?[1-9]|1[012])[-]([0-2][0-9]|3[01])[-]([0-9]{4})$");
        Matcher m = p.matcher(txtDepDate.getText());
        return m.matches();
    }
   
